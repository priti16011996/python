for val in range(10): #Stop conditions 
    print(val)

for val in range(1,11): #Start and stop conditions
    print(val)

for val in range(1,11,2): #Start, stop and step conditions
    print(val)

for val in range(2,11,2): #Start, stop and step conditions
    print(val)

for val in range(10,0,-1): #Start, stop and step conditions
    print(val)
